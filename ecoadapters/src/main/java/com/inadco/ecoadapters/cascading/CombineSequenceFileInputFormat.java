package com.inadco.ecoadapters.cascading;

import java.io.IOException;

import org.apache.hadoop.mapred.JobConf;
import org.apache.hadoop.mapred.Reporter;
import org.apache.hadoop.mapred.lib.CombineFileInputFormat;
import org.apache.hadoop.mapred.lib.CombineFileRecordReader;
import org.apache.hadoop.mapred.lib.CombineFileSplit;
import org.apache.hadoop.mapred.RecordReader;
import org.apache.hadoop.mapred.InputSplit;



public class CombineSequenceFileInputFormat<K, V> extends CombineFileInputFormat<K, V> {

	@SuppressWarnings({ "unchecked", "rawtypes" })
	@Override
	public RecordReader<K, V> getRecordReader(
			InputSplit split, JobConf job,
			Reporter reporter) throws IOException {
		return new CombineFileRecordReader(job, (CombineFileSplit)split, reporter, CombineSequenceFileRecordReader.class);
	}
	
}
